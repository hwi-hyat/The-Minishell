void	prompt(void)
{
	char	*str;

	init();
	while (1)
	{
		str = readline("shell$");
		if (!str)
			break ;
		printf("%s\n", str);
		free(str);
	}
}

int	main(int argc, char **argv, char **env)
{
	(void)argc;
	(void)argv;
	(void)env;
	prompt();

	return (0);
}
